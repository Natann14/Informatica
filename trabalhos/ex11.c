#include<stdio.h>
#include<stdlib.h>

int main(void)
{
    long long int x;
    int resultado=0;

    printf("\nDigite um número inteiro: \n");
    scanf("%lld", &x);


    for(int i=2; i <x/2; i++)
    {
        if(x%i==0)
        {
            resultado++;
            break;
        }
    }

    if(resultado==0)
        printf("\n%lld é um numero primo\n", x);
    else
        printf("\n%lld não é primo\n", x);
 
    return 0;
}
